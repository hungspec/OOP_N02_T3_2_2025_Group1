
public class NameNumber {
 private String lastName; 
 private String telNumber; 
 public NameNumber() {} 
 //constructor
 public NameNumber(String name, String num) {
 lastName = name;
 telNumber = num;
 }
 // phương thức
 public String getLastName() { return lastName;
 }
 public String getTelNumber() {
 return telNumber;
 }
}
